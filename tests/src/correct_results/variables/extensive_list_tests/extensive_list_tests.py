# extensive_list_tests (pyStage, converted from Scratch 3)

from pystage.en import Sprite, Stage

stage = Stage()
stage.add_backdrop('backdrop1')
stage.create_variable('my variable')
stage.create_list_variable("stage_list")
stage.initialize_list("stage_list", ['sun', 'stars', 'comets', 'planet'])
stage.show_builtinvariable("data_listcontents")
stage.set_monitor_position("data_listcontents", -235, 175)
stage.show_builtinvariable("data_listcontents")
stage.set_monitor_position("data_listcontents", 129, 40)
sprite1 = stage.add_a_sprite(None)
sprite1.set_name("Sprite1")
sprite1.set_x(0)
sprite1.set_y(0)
sprite1.go_to_back_layer()
sprite1.go_forward(1)
sprite1.add_costume('costume1', center_x=48, center_y=50)
sprite1.add_costume('costume2', center_x=46, center_y=53)
sprite1.add_sound('meow')
sprite1.create_list_variable("cat_list")
sprite1.initialize_list("cat_list", ['mice', 'bed', 'milk']) 

def when_program_starts_1(self):
    self.say_for_seconds("".join(["first item in cat_list is ", self.item_in_list("cat_list", 1)]), 2.0)
    self.say_for_seconds("".join(["first item in stage_list is ", self.item_in_list("stage_list", 1)]), 2.0)
    self.say_for_seconds("".join(["lenth of cat_list is ", self.length_of_list("cat_list")]), 2.0)
    self.say_for_seconds("".join(["length of stage_list is ", self.length_of_list("stage_list")]), 2.0)
    self.say_for_seconds("".join(["find bed in cat_list at location ", self.item_number_in_list("cat_list", "bed")]), 2.0)
    self.say_for_seconds("".join(["find comets in stage_list at location ", self.item_number_in_list("stage_list", "comets")]), 2.0)
    self.add_value_to_list("cat_list", "milk")
    self.add_value_to_list("stage_list", "planet")
    self.insert_value_to_list("cat_list", "earwax", 2)
    self.insert_value_to_list("stage_list", "asteroid", 5)
    self.replace_item_from_list("cat_list", 1, "Mice")
    self.replace_item_from_list("stage_list", 1, "Sun")
    self.delete_value_from_list("cat_list", 3)
    self.delete_value_from_list("stage_list", 2)
    self.say_for_seconds("".join(["does yarn exist in cat_list? ", self.list_contains_item("cat_list", "yarn")]), 2.0)
    self.say_for_seconds("".join(["does stars exist in stage_list? ", self.list_contains_item("stage_list", "stars")]), 2.0)
    self.say_for_seconds("".join(["does blah exist in cat_list? ", self.list_contains_item("cat_list", "blah")]), 2.0)
    self.say_for_seconds("".join(["does HELP exist in stage_list? ", self.list_contains_item("stage_list", "HELP")]), 2.0)
    self.say_for_seconds("".join(["value at bad index in cat_list: ", self.item_in_list("cat_list", -1)]), 2.0)
    self.say_for_seconds("".join(["value at bad index in stage_list: ", self.item_in_list("stage_list", 100)]), 2.0)
    self.delete_all_from_list("cat_list")
    self.delete_all_from_list("stage_list")

sprite1.when_program_starts(when_program_starts_1)

stage.play()
